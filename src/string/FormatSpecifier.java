package string;

public class FormatSpecifier {

	public static void main(String[] args) {
		System.out.printf("value of a is %d\n",10);
		System.out.printf("Formatted number is %.2f\n", 453.1274f);
        System.out.printf("Pie: %.3f\n", Math.PI);
        System.out.printf("value of c is %c\n",'b');
        System.out.printf("Hello my name is %s and I am %s\n", "Kunal", "Cool");
        System.out.printf("Hello %n I am \n aditya");
    }

}
